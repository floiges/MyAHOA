#pragma mark - methods
- (NSString *) getProvCode: (NSString*)provName
{
    NSString *areaCode = @"";
    
    for (NSString* key in provDict) {
        NSString* value = [provDict objectForKey:key];
        
        NSRange range = [provName rangeOfString:value];
        if(range.location != NSNotFound) {
            areaCode = key;
            break;
        }
        
    }
    
    return areaCode;
}

- (NSString *) getCityCode: (NSString*)cityName
{
    NSString *areaCode = @"";
    
    for (NSString* key in cityDict) {
        NSString* value = [cityDict objectForKey:key];
        
        NSRange range = [cityName rangeOfString:value];
        if(range.location != NSNotFound) {
            areaCode = key;
            break;
        }
        
    }
    
    return areaCode;
}


